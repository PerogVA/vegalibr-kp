def thread_ring_price(diam, pitch, side):
    """Цена резьбового кольца. side: 'пр' или 'не'."""
    return _THREAD_RING.get((float(diam), float(pitch), side.lower()))

def parse_thread_ring(name):
    """
    Парсит наименование резьбового кольца/пробки.
    Примеры: 'Калибр-кольцо М57×0,5-6g ПР', 'Калибр-пробка М5×0,8-6Н ПР'
    Возвращает (kind, diam, pitch, side) или None.
    kind: 'кольцо_резьбовое' | 'пробка_резьбовая'
    """
    if not name:
        return None
    nl = name.lower()

    # Определяем тип
    if 'кольцо' in nl:
        kind = 'кольцо_резьбовое'
    elif 'пробка' in nl and ('м' in nl or 'm' in nl):
        kind = 'пробка_резьбовая'
    else:
        return None

    # Ищем М<диаметр>×<шаг> или М<диаметр>x<шаг> (латинская x, кириллическая х, знак ×)
    m = re.search(r'м\s*(\d+[,.]?\d*)\s*[×xхХ]\s*(\d+[,.]?\d*)', nl)
    if not m:
        return None

    diam  = float(m.group(1).replace(',', '.'))
    pitch = float(m.group(2).replace(',', '.'))

    # Сторона: ПР или НЕ
    name_upper = name.upper()
    if ' НЕ' in name_upper or name_upper.endswith('НЕ'):
        side = 'не'
    else:
        side = 'пр'  # по умолчанию ПР

    return (kind, diam, pitch, side)

# ── Парсинг наименования ─────────────────────────────────────────────────────
def parse_caliber(name):
    """
    Разбирает наименование калибра.
    Возвращает (kind, size) или None.
    kind: 'скоба' | 'пробка' | 'кольцо_резьбовое' | 'пробка_резьбовая'
    """
    if not name:
        return None
    nl = name.lower().strip()

    # Резьбовое кольцо/пробка (содержит М<число>×<шаг>, любой разделитель)
    if re.search(r'м\s*\d+[,.]?\d*\s*[×xхХ]', nl):
        r = parse_thread_ring(name)
        if r:
            kind, diam, pitch, side = r
            return (kind, diam, pitch, side)

    # Скоба гладкая
    if 'скоба' in nl:
        m = re.search(r'скоба\s+(\d+[,.]?\d*)', nl)
        if m:
            size = float(m.group(1).replace(',', '.'))
            return ('скоба', size)

    # Пробка гладкая
    if 'пробка' in nl:
        m = re.search(r'пробка\s+(?:гладкая\s+)?(\d+[,.]?\d*)', nl)
        if m:
            size = float(m.group(1).replace(',', '.'))
            return ('пробка', size)

    return None

def calc_item_from_excel(name, qty, base_price, kal, include_kalib=True, discount=None):
    """Расчёт по ценам из Excel. Скидка на товар, калибровка без скидки."""
    d = discount if discount is not None else DISC
    disc    = round(base_price * d, 2)
    price_d = round(base_price - disc, 2)
    kal_use = kal if (include_kalib and kal is not None) else 0.0
    summa   = round(qty * (price_d + kal_use), 2)
    nds_s   = round(summa * NDS, 2)
    itogo   = round(summa + nds_s, 2)
    return {
        'name':    name,
        'ed':      'шт.',
        'qty':     qty,
        'price':   base_price,
        'disc':    disc,
        'price_d': price_d,
        'kal':     kal_use,
        'summa':   summa,
        'nds':     nds_s,
        'itogo':   itogo,
    }

def calc_item(name, qty, include_kalib=True, discount=None):
    """
    Считает одну позицию.
    Возвращает dict или None (если цена не определена).
    """
    parsed = parse_caliber(name)
    if parsed is None:
        return None

    # Резьбовые кольца/пробки
    if parsed[0] in ('кольцо_резьбовое', 'пробка_резьбовая'):
        kind, diam, pitch, side = parsed
        price = thread_ring_price(diam, pitch, side)
        if price is None:
            return None
        size = diam
        kal_full = 0  # калибровка резьбовых — из Excel или 0
    else:
        kind, size = parsed[0], parsed[1]
        if kind == 'скоба':
            price = skoba_price(size, name)
        else:
            price = proba_price(size)
        if price is None:
            return None
        kal_full = kalib_price(kind, size, name)

    kal = kal_full if include_kalib else 0.0
    d = discount if discount is not None else DISC
    disc    = round(price * d, 2)
    price_d = round(price - disc, 2)
    summa   = round(qty * (price_d + kal), 2)
    nds_s   = round(summa * NDS, 2)
    itogo   = round(summa + nds_s, 2)

    return {
        'name':    name,
        'ed':      'шт.',
        'qty':     qty,
        'price':   price,
        'disc':    disc,
        'price_d': price_d,
        'kal':     kal,
        'summa':   summa,
        'nds':     nds_s,
        'itogo':   itogo,
    }
